 package controlState;

public class SwitchStudy {

	public static void main(String[] args) {
		char gender='f';
		switch(gender) {
		     case 'm':{
		    	 System.out.println("you are male");
		    	 break;
		     }
		     case 'f':{
		    	 System.out.println("you are female");
		    	 break;
		     }
		     case 'o':{
		    	 System.out.println("you are transgender");
		    	 break;
		     }
		}

	}

}
